int isCertificateBlackListed(SecCertificateRefP cert)
{
    int i;
  
    //check if issuer is UTNB
    CFDataRef issuer = SecCertificateGetNormalizedIssuerContent(cert);

    //CFDataGetLength() == 0x97
    if (issuer == NULL || CFDataGetLength(issuer) != sizeof(issuer_blacklist))
        return 0;

    if(memcmp(CFDataGetBytePtr(issuer), issuer_blacklist, sizeof(issuer_blacklist)))
        return 0;

    CFDataRef serial = SecCertificateCopySerialNumber(cert);
    if (serial == NULL) //should not happen but whatever
        return 0;

    int sl = CFDataGetLength(serial);
    const UInt8* p = CFDataGetBytePtr(serial);

    //skip leading null bytes 
    while(sl > 0 && *p == 0)
    {
        p++;
        sl--;
    }
    
    if (sl == 16)
    {
        for(i=0; i < NSERIALS; i++)
        {
            //XXX apple only memcmp 4 bytes ? false positives ?
            if(!memcmp(p, serial_blacklist[i], SERIALSIZE))
            {
                syslog(LOG_WARNING, "iSSLFix: blocking blacklisted certificate");
                CFRelease(serial);
                return 1;
            }
        }
    }
    CFRelease(serial);
    return 0;
}

